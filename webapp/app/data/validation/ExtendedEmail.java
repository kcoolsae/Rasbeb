package data.validation;

import javax.validation.Constraint;
import javax.validation.Payload;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

import static java.lang.annotation.ElementType.*;
import static java.lang.annotation.RetentionPolicy.*;

/**
 * Validation annotation to indicate that the field should
 */
@Target({FIELD})
@Retention(RUNTIME)
@Constraint(validatedBy = ExtendedEmailValidator.class)
@play.data.Form.Display(name="constraint.extendedemail")
public @interface ExtendedEmail {

    // based on http://stackoverflow.com/questions/8115106/how-to-create-a-custom-validator-in-play-framework-2-0

    String message() default ExtendedEmailValidator.message;
    Class<?>[] groups() default {};
    Class<? extends Payload>[] payload() default {};
}
